
namespace EVEMon.Common.Controls
{
    /// <summary>
    /// Defines the action that the parent should take upon notification of an API Error Troubleshooter resolution.
    /// </summary>
    public enum ResolutionAction
    {
        /// <summary>
        /// No action.
        /// </summary>
        None,
        /// <summary>
        /// Close the window.
        /// </summary>
        Close,
        /// <summary>
        /// Hide the troubleshooter.
        /// </summary>
        HideTroubleshooter
    }
}
